                        <li style="padding: 10px 0;">
                            <strong>What is OSFAC-DMT?</strong><br />
                            OSFAC-DMT (OSFAC Data Management Tool) is a tool which allows users to interactively make satellite 
                            images requests from OSFAC database.
                            In other words, the tool allows users to post a request of satellite images remotely and that request 
                            is automatically sent to OSFAC's database administrator via Internet. 
                        </li>
                        <li style="padding: 10px 0;">
                            <strong>Why OSFAC-DMT?</strong><br />
                            As there is often low Internet connectivity and low bandwidth in the region, OSFAC continues 
                            to ensure the physical distribution of satellite images in the subregion to any enterprise or person 
                            interested in using geospatial data. As the number of requests, the number of images, and the size of the database 
                            is continually rising, OSFAC has developed a tool, OSFAC-DMT,  which facilitates the work of 
                            archiving newly acquired remote sensing imagery, managing online satellite images request, searching 
                            and downloading of images, and statistics production of distributed images.   
                        </li>
                        <li style="padding: 10px 0;">
                            <strong>Where can I download OSFAC-DMT Desktop?</strong><br />
                            You should go to <a href="desktop.php">http://dmt.osfac.net/pages/desktop.php</a> and provide your Email address before getting the download button available.
                        </li>
                        <li style="padding: 10px 0;">
                            <strong>What is the main difference between Online and Desktop version of OSFAC-DMT?</strong><br />
                            In addition to helping users to request satellite images, OSFAC-DMT Desktop allows users to view and manipulate spatial data 
                            easily and in an intuitive manner. It allows users to view, edit, save, and analyze spatial data.   
                            Moreover, it handles vector data and displays raster data as well.
                        </li>
                        <li style="padding: 10px 0;">
                            <strong>What are the countries covered by OSFAC-DMT data?</strong><br />
                            OSFAC database covers the entire Congo Basin (DR Congo, Congo, Cameroon, Equatorial Guinea, Central African Republic, 
                            Gabon) plus Angola and Chad.
                        </li>
                        <li style="padding: 10px 0;">
                            <strong>What kind of satellite images could be found in OSFAC-DMT?</strong><br />
                            OSFAC’s database contains more than 50,000 images over 10 TB of imagery : <strong>Landsat</strong> (1972-2012), 
                            <strong>ASTER</strong> (2000-2009), <strong>SRTM</strong> (2000), <strong>Landsat Mosaics</strong> (2004-2005), 
                            <strong>ASTER GDEM</strong> (2009), <strong>RADARSAT MOSAIC</strong>, 
                            <strong>Quickbird</strong> (2008-2009), <strong>Controlled Image Base</strong> (CIB), <strong>IKONOS</strong>, 
                            <strong>WordView</strong>, <strong>EarthSat NaturalVue</strong> (Landsat 7-derived mosaics).
                            Some of these images are free to download but others require licenses.
                        </li>
