                <img style="margin-left: 10px; float: right;" src="../images/dmt.png" alt="image2" width="180" height="168" />
                <p align="justify">
                    OSFAC’s database contains more than 50,000 images or over 10 TB of imagery : <strong>Landsat</strong> (1972-2012), 
                    <strong>ASTER</strong> (2000-2009), <strong>SRTM</strong> (2000), <strong>Landsat Mosaics</strong> (2004-2005), 
                    <strong>ASTER GDEM</strong> (2009), <strong>RADARSAT MOSAIC</strong>, 
                    <strong>Quickbird</strong> (2008-2009), <strong>Controlled Image Base</strong> (CIB), <strong>IKONOS</strong>, 
                    <strong>WordView</strong>, <strong>EarthSat NaturalVue</strong> (Landsat 7-derived mosaics).
                </p><br/>
                <div style="color: #000000;font-size: 13px;font-weight: bold;">Why OSFAC-DMT?</div><br/>
                <p align="justify">
                    As there is often low Internet connectivity and low bandwidth in the region, OSFAC continues 
                    to ensure the physical distribution of satellite images in the subregion to any enterprise or person 
                    interested in using geospatial data. As the number of requests, the number of images, and the size of the database 
                    is continually rising, OSFAC has developed a tool, OSFAC-DMT,  which facilitates the work of 
                    archiving newly acquired remote sensing imagery, managing online satellite images request, searching 
                    and downloading of images, and statistics production of distributed images.
                </p><br/>
                <p align="justify">
                    <strong>OSFAC-Data Management Tool</strong> is a tool which allows users to interactively make satellite 
                    images requests from OSFAC database.
                    In other words, the tool allows users to post a request of satellite images remotely and that request 
                    is automatically sent to OSFAC's database administrator via Internet. 
                    <br/><br/>Note:
                    <span style="color: #ff0000;">
                        <strong>
                            This tool does not allow you to download satellite images, just to make a request of them.
                        </strong>
                    </span>
                </p><br/>
