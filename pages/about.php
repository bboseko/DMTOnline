                <p align="justify">
                    One of OSFAC’s main activities is to disseminate, free of charge, satellite images and 
                    derivatives throughout the Congo Basin received from its partners such as NASA, USGS and USFS, UMD, ... 
                    OSFAC currently has a database of images from various satellites images with different varying spatial and 
                    temporal resolution, covering the entire Congo Basin. 
